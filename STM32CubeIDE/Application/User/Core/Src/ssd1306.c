void SSD1306_Puts(const char *str) {
    while (*str) {
        uint8_t ch = *str++;

        // 遇到换行符，强制另起一行
        if (ch == '\n' || ch == '\r') {
            cursor_x = 0;
            cursor_y += 8;
            continue;
        }

        // 仅处理字库范围内的可见字符
        if (ch < 32 || ch > 126) ch = 32;
        uint8_t idx = ch - 32;

        for (uint8_t col = 0; col < 5; col++) {
            uint8_t byte = font5x7[idx][col];
            for (uint8_t row = 0; row < 8; row++) {
                if (byte & (1 << row)) {
                    uint16_t x = cursor_x + col;
                    uint16_t y = cursor_y + row;
                    if (x < SSD1306_WIDTH && y < SSD1306_HEIGHT) {
                        buffer[x + (y / 8) * SSD1306_WIDTH] |= (1 << (y % 8));
                    }
                }
            }
        }
        cursor_x += 6;

        // 自动换行保护
        if (cursor_x > SSD1306_WIDTH - 6) {
            cursor_x = 0;
            cursor_y += 8;
        }

        // 触底清屏保护
        if (cursor_y > SSD1306_HEIGHT - 8) {
            cursor_y = 0;
            SSD1306_Clear();
        }
    }
}
