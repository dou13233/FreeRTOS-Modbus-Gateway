static void SSD1306_WriteData(uint8_t *data, uint16_t len) {
    // 放弃使用 malloc，直接改为局部栈数组，防止标准堆内存枯竭
    uint8_t buf[SSD1306_WIDTH + 1];

    buf[0] = 0x40;   // 控制字节 0x40 表示数据
    if (len > SSD1306_WIDTH) len = SSD1306_WIDTH; // 增加防越界保护
    memcpy(buf + 1, data, len);

    HAL_I2C_Master_Transmit(&hi2c1, SSD1306_I2C_ADDR, buf, len + 1, 100);
}
// 初始化
void SSD1306_Init(void) {
    // 复位序列（根据数据手册）
 //   HAL_Delay(100);
	osDelay(100);
    SSD1306_WriteCmd(0xAE); // 关闭显示
    SSD1306_WriteCmd(0xD5); // 设置时钟分频
    SSD1306_WriteCmd(0x80);
    SSD1306_WriteCmd(0xA8); // 设置多路复用比
    SSD1306_WriteCmd(0x3F);
    SSD1306_WriteCmd(0xD3); // 显示偏移
    SSD1306_WriteCmd(0x00);
    SSD1306_WriteCmd(0x40); // 起始行
    SSD1306_WriteCmd(0x8D); // 电荷泵
    SSD1306_WriteCmd(0x14);
    SSD1306_WriteCmd(0x20); // 内存寻址模式
    SSD1306_WriteCmd(0x00);
    SSD1306_WriteCmd(0xA1); // 段重映射
    SSD1306_WriteCmd(0xC8); // COM 扫描方向
    SSD1306_WriteCmd(0xDA); // COM 引脚配置
    SSD1306_WriteCmd(0x12);
    SSD1306_WriteCmd(0x81); // 对比度
    SSD1306_WriteCmd(0xCF);
    SSD1306_WriteCmd(0xD9); // 预充电周期
    SSD1306_WriteCmd(0xF1);
    SSD1306_WriteCmd(0xDB); // VCOMH 取消选择电平
    SSD1306_WriteCmd(0x40);
    SSD1306_WriteCmd(0xA4); // 全局显示开启
    SSD1306_WriteCmd(0xA6); // 正常显示（非反色）
    SSD1306_WriteCmd(0xAF); // 开启显示

    SSD1306_Clear();
    SSD1306_Update();
}

// 清屏（所有像素清为 0）
void SSD1306_Clear(void) {
    memset(buffer, 0, sizeof(buffer));
}

// 将显存内容发送到屏幕
void SSD1306_Update(void) {
    // 设置页地址模式（每页8行）
    for (uint8_t page = 0; page < 8; page++) {
        SSD1306_WriteCmd(0xB0 + page);          // 设置页地址
        SSD1306_WriteCmd(0x00);                // 低列地址
        SSD1306_WriteCmd(0x10);                // 高列地址
        SSD1306_WriteData(&buffer[page * SSD1306_WIDTH], SSD1306_WIDTH);
    }
}

